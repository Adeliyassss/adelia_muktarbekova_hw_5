package com.company;

public class Boss {
        private int healthOfBoss;
        private int damageOfBoss;
        private String typeOfProtection;

        public int getHealthOfBoss() {
            return healthOfBoss;
        }

        public void setHealthOfBoss(int healthOfBoss) {
            this.healthOfBoss = healthOfBoss;
        }

        public String getTypeOfProtection() {
            return typeOfProtection;
        }

        public void setTypeOfProtection(String typeOfProtection) {
            this.typeOfProtection = typeOfProtection;
        }

        public int getDamageOfBoss() {
            return damageOfBoss;
        }

        public void setDamageOfBoss(int damageOfBoss) {
            this.damageOfBoss = damageOfBoss;
        }
    }

